import java.util.Scanner;

class Main{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(sc.hasNext())
        {
            int i = sc.nextInt();
            int j = sc.nextInt();
            System.out.println(i+j);
        }
    }
}